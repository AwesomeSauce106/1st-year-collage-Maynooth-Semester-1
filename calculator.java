public class calculator {
    public static void main(String[] args) {
        char math = '*';
        int num1 = 15, num2 = 3, answer = 0;

        switch (math) {
            case '+':
                answer = num1 + num2;
                System.out.println(answer);
                break;
            case '-':
                answer = num1 + num2;
                System.out.println(answer);
                break;
            case '*':
                answer = num1 + num2;
                System.out.println(answer);
                break;
            case '/':
                answer = num1 + num2;
                System.out.println(answer);
                break;
        }
    }
}
