public class driverCategory {
    public static void main(String[] args) {
        char license = 'a';

        switch (license) {
            case 'a':
                System.out.println("Motorcycle");
                break;
            case 'b':
                System.out.println("Car");
                break;
            case 'c':
                System.out.println("Truck");
                break;
            case 'd':
                System.out.println("Bus");
                break;
        }
    }
}
